    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed

        if (idT.getText().isEmpty() || nameT.getText().isEmpty() || dobT.getText().isEmpty()) {
            Utils.showMsg("Error! No field can be empty");
        } else {

            Producer producer = new Producer(Integer.parseInt(idT.getText()), nameT.getText(), dobT.getText());

            if (isUpdate) {

                ProducerDAO.updateProducer(producer);
                insertProducers();
                resetValues();
                Utils.showMsg("Successfully updated  producer");

            } else {

                Producer isExist = ProducerDAO.getProducerById(Integer.parseInt(idT.getText()));
                if (isExist == null) {
                    ProducerDAO.createProducer(producer);
                    insertProducers();
                    resetValues();
                    Utils.showMsg("Successfully created new producer");

                } else {
                    Utils.showMsg("A producer already exists with this id.");

                }

            }

        }


    }//GEN-LAST:event_saveBtnActionPerformed

    private void producerTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_producerTableMouseClicked
        int index = producerTable.getSelectedRow();
        if (index != -1) {

            int id = (int) producerTable.getValueAt(index, 0);
            String name = (String) producerTable.getValueAt(index, 1);
            String dob = (String) producerTable.getValueAt(index, 2);
            idT.setEditable(false);
            saveBtn.setText("Update");

            idT.setText(id + "");
            nameT.setText(name);
            dobT.setText(dob);
            isUpdate = true;

            ArrayList<Movie> movies = ProducerDAO.getMoviesByProducerId(id);

            insertMovies(movies);

        }
    }//GEN-LAST:event_producerTableMouseClicked

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int index = producerTable.getSelectedRow();
        if (index == -1) {
            Utils.showMsg("Please first select any producer to delete");
        } else {
            ProducerDAO.deleteProducer((int) producerTable.getValueAt(index, 0));
            resetValues();
            Utils.showMsg("Successfully deleted Producer");
            movieModel.getDataVector().removeAllElements();
            movieTable.revalidate();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

