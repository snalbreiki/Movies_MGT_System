    void resetValues() {

        isUpdate = false;
        producerTable.clearSelection();
        idT.setText("");
        nameT.setText("");
        dobT.setText("");
        insertProducers();
        producerTable.clearSelection();
        movieTable.clearSelection();
        idT.setEditable(true);
        saveBtn.setText("Save");

    }

