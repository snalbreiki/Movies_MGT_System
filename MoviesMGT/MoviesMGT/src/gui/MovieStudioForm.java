    void resetValues() {
        id = -1;
        nameT.setText("");
        addressT.setText("");
        isUpdate = false;
        saveBtn.setText("Save");
        studioTable.clearSelection();
        movieTable.clearSelection();
        insertMovieStudios();
        
    }

