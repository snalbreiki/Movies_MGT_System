    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        
        if (nameT.getText().isEmpty() || addressT.getText().isEmpty()) {
            Utils.showMsg("Error! No field can be empty");
        } else {
            
            if (isUpdate) {
                MovieStudio studio = new MovieStudio(id, nameT.getText(), addressT.getText());
                MovieStudioDAO.updateMovieStudio(studio);
                resetValues();
                Utils.showMsg("Successfully updated Movie Studio");
                
            } else {
                MovieStudio studio = new MovieStudio(nameT.getText(), addressT.getText());
                MovieStudioDAO.createMovieStudio(studio);
                resetValues();
                Utils.showMsg("Successfully added Movie Studio");
            }
            
        }

    }//GEN-LAST:event_saveBtnActionPerformed

