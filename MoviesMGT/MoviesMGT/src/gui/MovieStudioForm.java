    private void studioTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_studioTableMouseClicked
        int index = studioTable.getSelectedRow();
        if (index != -1) {
            isUpdate = true;
            saveBtn.setText("Update");
            int s_id = (int) studioTable.getValueAt(index, 0);
            String name = (String) studioTable.getValueAt(index, 1);
            String address = (String) studioTable.getValueAt(index, 2);
            
            id = s_id;
            nameT.setText(name);
            addressT.setText(address);
            
            ArrayList<Movie> movies = MovieStudioDAO.getMoviesByMovieStudioId(s_id);
            insertMovies(movies);
            
            
            
        }
    }//GEN-LAST:event_studioTableMouseClicked

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int index = studioTable.getSelectedRow();
        
        if (index == -1) {
            
            Utils.showMsg("Please first select ant studio to delete");
        } else {
            
            MovieStudioDAO.deleteMovieStudio((int) studioTable.getValueAt(index, 0));
            resetValues();
            Utils.showMsg("Successfully deleted Movie Studio");
            
            movieModel.getDataVector().removeAllElements();
            movieTable.revalidate();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

