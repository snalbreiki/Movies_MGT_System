    private void directorTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_directorTableMouseClicked
        int index = directorTable.getSelectedRow();
        if (index != -1) {
            isUpdate = true;
            saveBtn.setText("Update");

            int id = (int) directorTable.getValueAt(index, 0);
            String name = (String) directorTable.getValueAt(index, 1);
            String dob = (String) directorTable.getValueAt(index, 2);

            idT.setEditable(false);

            idT.setText(id + "");
            nameT.setText(name);
            dobT.setText(dob);
            
            ArrayList<Movie> movies = DirectorDAO.getMoviesByDirectorId(id);
            insertMovies(movies);

        }
    }//GEN-LAST:event_directorTableMouseClicked

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int index = directorTable.getSelectedRow();

        if (index == -1) {
            Utils.showMsg("Please first select any director to delete");

        } else {

            DirectorDAO.deleteDirector((int) directorTable.getValueAt(index, 0));
            resetValues();
            Utils.showMsg("Successfully delete director");
            movieModel.getDataVector().removeAllElements();
            movieTable.revalidate();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

