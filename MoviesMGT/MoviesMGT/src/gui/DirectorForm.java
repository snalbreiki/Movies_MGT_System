    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed

        if (idT.getText().isEmpty() || nameT.getText().isEmpty() || dobT.getText().isEmpty()) {
            Utils.showMsg("Error! No field can be empty");
        } else {

            Director director = new Director(Integer.parseInt(idT.getText()), nameT.getText(), dobT.getText());

            if (isUpdate) {

                DirectorDAO.updateDirector(director);
                resetValues();
                Utils.showMsg("Successfully updated director");

            } else {
                Director isExist = DirectorDAO.getDirectorById(Integer.parseInt(idT.getText()));

                if (isExist == null) {

                    DirectorDAO.createDirector(director);
                    resetValues();
                    Utils.showMsg("Successfully added director");

                } else {

                    Utils.showMsg("Director with this ID already exists");
                }
            }

        }

    }//GEN-LAST:event_saveBtnActionPerformed

