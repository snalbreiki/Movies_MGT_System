    void resetValues() {
        idT.setText("");
        nameT.setText("");
        dobT.setText("");
        directorTable.clearSelection();
        movieTable.clearSelection();
        isUpdate = false;
        idT.setEditable(true);
        saveBtn.setText("Save");
        insertDirectors();

    }

