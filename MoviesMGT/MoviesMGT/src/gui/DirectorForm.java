    void insertDirectors() {
        directorModel.getDataVector().removeAllElements();
        directorTable.revalidate();

        for (Director dir : DirectorDAO.getAllDirectors()) {
            directorModel.addRow(new Object[]{dir.getId(), dir.getName(), dir.getDateOfBirth()});
        }

    }

    void insertMovies(ArrayList<Movie> moviesArrayList) {
        movieModel.getDataVector().removeAllElements();

        movieTable.revalidate();

        for (Movie movie : moviesArrayList) {
            movieModel.addRow(new Object[]{movie.getTitle(), movie.getYearOfRelease()});
        }
    }

